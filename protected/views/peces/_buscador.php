<div class="tooltiptext" id="veda_tip">
	<p class="titblue">Vedas</p>
	<p>Con el fin de optimizar el aprovechamiento de la pesca desde un punto de vista biol&oacute;gico y socioecon&oacute;mico, la Comisi&oacute;n Nacional de Acuacultura y Pesca (CONAPESCA) ha establecido regulaciones y actos administrativos para proteger a las especies comerciales. Las vedas consisten en prohibiciones de pesca durante periodos de tiempo o sitios espec&iacute;ficos mediante acuerdos o normas oficiales, estas tienen la finalidad de proteger a una o varias especies durante los periodos de reproducci&oacute;n o crianza.</p>
	<p class="titblue">Tipos de veda en M&eacute;xico:</p>
    <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Veda permanente:</strong> La especie no puede ser explotada en ningún momento del a&ntilde;o de forma indefinida.</p>
    <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Veda temporal fija:</strong> Se establece en un periodo definido y se repite anualmente.</p>
    <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Veda temporal variable:</strong> Se establece por un periodo definido pero es actualizada anualmente.</p>
</div>

<div class="tooltiptext" id="arte_de_pesca_tip">
	<p class="titblue">Artes de pesca</p>
    <p>Las artes de pesca son los diferentes métodos de captura o técnicas con las que los peces son atrapados.  
		Existen 4 tipos principales de arte y de cada uno de ellos tienen variables y combinaciones diferentes:
	</p>
	<p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> L&iacute;neas:</strong> Este arte consiste en un hilo
		con un anzuelo con carnada en su punta final. Existen variantes de
		la l&iacute;nea como: l&iacute;nea de anzuelo, l&iacute;nea
		cabrillera de fondo, l&iacute;nea de mano, troleado y carpiota o
		pollito.</p>
        <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Palangres:</strong> Es una cuerda larga con
		varios anzuelos con carnada sujetados a ciertos intervalos. Existen
		diferentes tipos de palangres como palangres escameros, palangre
		tiburonero, palangre viajero de fondo, rosario o ristra, dependiendo
		de las especies &ldquo;objetivo&rdquo;.</p>
       <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Redes:</strong> Las redes son mallas construida de hilos finos. Los peces quedan
		atrapados por las agallas o entre la cabeza y el cuerpo. Existen
		diferentes tipos de redes entre ellas est&aacute;n la red agallera,
		atarraya, red caritera, red corvinera y red sardinera.</p>
        <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Trampas y nasas:</strong> Son cajas o compartimientos a los que son
		atra&iacute;dos los peces. Las formas t&iacute;picas de las trampas
		y nasas, son cajas, conos, cilindros, esferas o botellas. Su
		tama&ntilde;o var&iacute;a seg&uacute;n la especie a capturar.
		Tienen aperturas en forma de embudos o cu&ntilde;as para que el pez
		ingrese f&aacute;cilmente pero tenga una baja probabilidad de
		escape. Existen diferentes tipos de trampas y nasas, como la nasa o
		trampa langostera, casitas cubanas, trampa para bacalao, trampa para
		pulpo.</p>
</div>

<div class="tooltiptext" id="tipo_pesca_tip">
	<p class="titblue">Tipos de artes de pesca</p>
    <p>Algunas de las artes de pesca tienen un fuerte impacto sobre la biodiversidad marina 
	y su ambiente y otras no lo tienen, por lo que es determinante que el consumidor conozca la informaci&oacute;n sobre 
	el tipo de arte que es utilizado por especie, para esto se han separado las artes de pesca en dos grupos:
    </p>
	<p><img src="http://www.biodiversidad.gob.mx/usos/alimentacion/images/imgPecesAmigable.png" width="17" height="17"><strong> Selectiva</strong> &nbsp;<br> Algunos tipos de artes de
		pesca de bajo impacto: Red de arrastre para camarones
		RS-INP-MEX-110&acute;para embarcaciones mayores en el Golfo de
		California, red agallera para lisa y liseta o lebrancha,
		l&iacute;nea de mano para peces pel&aacute;gicos y demersales,
		curric&aacute;n para pesca de especies pel&aacute;gicas, palangre
		para peces pel&aacute;gicos costeros, palangre para mero, palangre
		para la pesca de tiburones pel&aacute;gicos del Pac&iacute;fico
		central mexicano, a bordo de embarcaciones de mediana altura,
		palangre para at&uacute;n: Golfo de M&eacute;xico y Caribe.</p>
	<p><img src="http://www.biodiversidad.gob.mx/usos/alimentacion/images/imgPecesNoAmigable.png" width="17" height="17"> 
		<strong>No selectiva</strong><br> 
		Se capturan diferentes especies y tama&ntilde;os que no son el objetivo. Puede afectar delfines, tortugas y 
		aves, da&ntilde;ar el ambiente y destruir los ecosistemas marinos.<br> 
		Algunos tipos de artes de
		pesca de alto impacto para el ambiente son el chinchorro playero,
		red de enmalle para tiburones y rayas en embarcaciones menores, red
		agallera para escama pel&aacute;gica, red agallera para escama de
		fondo, red agallera para esteros y l&iacute;nea de costa, palangre
		para embalses, palangre escama de esteros, palangre para especies de
		fondo, palangre para tibur&oacute;n oce&aacute;nico y pez espada.</p>
</div>

<div class="tooltiptext" id="carta_nacional_tip">
	<p class="titblue">Carta Nacional Pesquera (CNP)</p>
    <p>Es el &uacute;nico documento oficial e instrumento regulatorio pesquero ambiental que indica el estado de salud de organismos marinos comerciales en M&eacute;xico, adem&aacute;s plantea restricciones y limites en algunas pesquer&iacute;as.</p>
    <p>En base a estudios cient&iacute;ficos la carta agrupa en tres categor&iacute;as a los grupos de peces explotados:</p>
    <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> En deterioro:</strong> El esfuerzo pesquero es excesivo sobre las poblaciones de peces. Existe una disminuci&oacute;n notable en la biomasa y una capacidad reducida de reclutamiento.</p>
    <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> M&aacute;ximo aprovechamiento permisible:</strong> El nivel de utilizaci&oacute;n de las poblaciones de peces se considera &oacute;ptimo y 
			no es posible aumentar la captura sin sobreexplotaci&oacute;n.</p>
            <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6"
			height="6"> Con potencial de desarrollo:</strong> Existe la posibilidad de aumentar las capturas de las poblaciones de peces, 
			incrementando el esfuerzo pesquero, siempre y cuando se realice de manera precautoria y controlada.</p>
		
		<p>De acuerdo a los sitios de pesca de cada especie, la carta nacional ha regionalizado los sitios de acuerdo a zonas y n&uacute;meros:</p>
        <p class="titblue">Pac&iacute;fico</p>
		<p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Zona 1:</strong> Pen&iacute;nsula de Baja California (Costa del Pac&iacute;fico de Baja California y Baja California Norte.</p>
		<p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Zona 2:</strong> Golfo de California (Baja California, Baja California Sur, Sonora, Sinaloa, Nayarit).</p>
		<p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Zona 3:</strong> Pac&iacute;fico centro-sur (Jalisco, Colima, Michoac&aacute;n, Guerrero, Oaxaca, Chiapas).</p>
		<p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Zona 4:</strong> Archipi&eacute;lago de las Islas Revillagigedo.</p>

		<p class="titblue">Golfo de M&eacute;xico y Caribe</p>
        <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Zona 1:</strong> Tamaulipas y Veracruz.</p>
        <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Zona 2:</strong> Tabasco, Campeche y Yucat&aacute;n.</p>
        <p><strong><img src="/peces/imagenes/aplicacion/bp.png" width="6" height="6"> Zona 3:</strong> Quintana Roo.</p>
        
</div>

